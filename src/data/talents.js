function toRoman(value) {
  const numerals = [
    "", "II", "III", "IV", "V", "VI", "VII", "VIII", "IX", "X",
    "XI", "XII", "XIII", "XIV", "XV", "XVI", "XVII", "XVIII", "XIX", "XX",
  ];
  return numerals[value - 1] || `${value}`;
}

function levelId(baseId, level) {
  return level === 1 ? baseId : `${baseId}_${level}`;
}

function levelName(name, level) {
  return level === 1 ? name : `${name} ${toRoman(level)}`;
}

function round(value) {
  return Number(Number(value || 0).toFixed(6));
}

function buildBandCurve(bands = []) {
  const values = [];
  let total = 0;
  for (const band of bands) {
    for (let idx = 0; idx < band.count; idx += 1) {
      total += band.step;
      values.push(round(total));
    }
  }
  return values;
}

function unlockAt(start, step, level) {
  return start + (level - 1) * step;
}

function basicCost(level) {
  if (level <= 8) return 1;
  if (level <= 14) return 2;
  if (level <= 18) return 3;
  return 4;
}

function gameplayCost(level) {
  if (level <= 3) return 2;
  return 3;
}

function keystoneCost(level) {
  return [4, 5, 6][level - 1] || 6;
}

function buildTalentChain(def) {
  const talents = [];
  for (let level = 1; level <= def.levels; level += 1) {
    const previousId = level > 1 ? levelId(def.baseId, level - 1) : null;
    const value = def.values ? def.values[level - 1] : undefined;
    const talent = {
      id: levelId(def.baseId, level),
      classId: def.classId,
      specId: def.specId ?? null,
      name: levelName(def.name, level),
      description: typeof def.description === "function" ? def.description(level, value) : def.description,
      type: def.type,
      ...(def.displayType ? { displayType: def.displayType } : {}),
      trigger: typeof def.trigger === "function" ? def.trigger(level, value) : def.trigger,
      effect: def.effect(level, value),
      unlockCondition: { stat: "level", value: def.unlockForLevel(level) },
      cost: def.costForLevel(level),
      ...(def.tags ? { tags: def.tags } : {}),
      ...(def.exclusiveGroup ? { exclusiveGroup: def.exclusiveGroup } : {}),
      ...(previousId ? { replaces: previousId } : {}),
    };
    talents.push(talent);
  }
  return talents;
}

