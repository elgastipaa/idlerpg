function createBoss({
  id,
  name,
  family,
  tier,
  intro,
  mechanics,
  seedArchetype,
  slotProfile,
  huntLabel,
  huntDescription,
  favoredFamilies,
  favoredStats,
}) {
  const slotBaseline =
    tier >= 25 ? BOSS_SLOT_BASELINES[5]
      : tier >= 20 ? BOSS_SLOT_BASELINES[4]
        : tier >= 15 ? BOSS_SLOT_BASELINES[3]
          : tier >= 10 ? BOSS_SLOT_BASELINES[2]
            : BOSS_SLOT_BASELINES[1];

  return {
    id,
    name,
    family,
    tier,
    hp: slotBaseline.hp,
    maxHp: slotBaseline.hp,
    damage: slotBaseline.damage,
    defense: slotBaseline.defense,
    xpReward: slotBaseline.xpReward,
    goldReward: slotBaseline.goldReward,
    essenceReward: slotBaseline.essenceReward,
    guaranteedRarityFloor: slotBaseline.guaranteedRarityFloor,
    dropRarityBonus: { ...slotBaseline.dropRarityBonus },
    isBoss: true,
    intro,
    mechanics,
    seedArchetype,
    slotProfile,
    huntLabel,
    huntDescription,
    favoredFamilies,
    favoredStats,
  };
}

